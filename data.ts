const shuffleArray = <T,>(array: T[]): T[] => {
  const result = [...array];
  for (let i = result.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [result[i], result[j]] = [result[j], result[i]];
  }
  return result;
};

const generateQuestions = (raw: { q: string, a: string }[], category: Category): Question[] => {
  const allAnswers = raw.map(item => item.a);
  
  return raw.map((item, index) => {
    let distractors = allAnswers
      .filter(a => a !== item.a)
      .sort(() => 0.5 - Math.random())
      .slice(0, 3);
      
    while (distractors.length < 3) {
      distractors.push(`Սխալ պատասխան ${distractors.length + 1}`);
    }

    const options = shuffleArray([item.a, ...distractors]);
    
    return {
      id: index + (category === Category.CSS ? 1000 : category === Category.SECURITY ? 2000 : category === Category.JAVASCRIPT ? 3000 : 0),
      category,
      question: item.q,
      correctAnswer: item.a,
      options
    };
  });
};

